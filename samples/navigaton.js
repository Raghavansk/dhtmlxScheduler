function loadContent(container, link) {
  $.get(link, function (data, status, request) {
    var html = $.parseHTML(data, null, true);
    var title = $("<h4 class='title' />");
    var content = $("<div class='content' />");

    $('<base href="' + link + '" />').appendTo(content);
          
    html.forEach(function (item, index, array) {
      var $item = $(item);
      switch (item.nodeName) {
        case "TITLE":
          title.text(item.text);
          break;
        case "#text":
          if (item.textContent.replace(/^\s+/, '').replace(/\s+$/, '') != "") {
            content.append(item);
          }
          break;
        case "SCRIPT":
          //if ($item.attr('src') != "../../codebase/dhtmlxscheduler.js") {
            content.append(item);
          //}
          break;
        case "LINK":
          if ($item.attr('href') != "../../codebase/dhtmlxscheduler.css") {
            content.append(item);
          }
          break;
        case "STYLE":
          var selector = "html, body";
          var pattern = new RegExp(selector.replace(/\./g, "\\.") + "\\s*{[^}]*?}", "gim");
          var style = item.outerHTML.replace(pattern, "");
          content.append($(style));
          break;
        case "META":
          // ignore meta tags  
          break;
        case "DIV":
        case "FORM":
        case "IFRAME":
          content.append(item);
          break;
        default:
          content.append(item);
          console.log(item);
      }
    });

    $(container).html('').append(title).append(content);
    init();
  });
}